package easy;

public class Package {
	int weight;
	int value;
	String name;
	public Package(int weight, int value, String name) {
		this.weight = weight;
		this.value = value;
		this.name = name;
	}
	public int getWeight() {
		return weight;
	}
	public int getValue() {
		return value;
	}
	public String getName() {
		return name;
	}
}
